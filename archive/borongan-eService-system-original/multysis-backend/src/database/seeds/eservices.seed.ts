export const seedEServices = async () => {
  console.log('Seeding E-Services...');

  // Clear existing eservices first
  await prisma.eService.deleteMany({});
  console.log('Cleared existing E-Services.');

  for (const serviceData of eServicesData) {
    await prisma.eService.create({
      data: {
        ...serviceData,
        isActive: true,
      },
    });
    console.log(`Created E-Service: ${serviceData.name}`);
  }

  console.log(`E-Services seeding completed! Total: ${eServicesData.length} services.`);
};
